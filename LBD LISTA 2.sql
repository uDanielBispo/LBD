INNER JOIN TB_CLIENTE C
ON P.CODCLIENTE = C.CODCLIENTE AND P.CODVENDEDOR IN
(SELECT CODVENDEDOR FROM TB_PEDIDO WHERE CODVENDEDOR = 5);

--4. Quais vendedores não fizeram mais de 2 pedidos.

SELECT V.NOMEVENDEDOR, COUNT(V.CODVENDEDOR) AS CONTAGEM FROM TB_VENDEDOR V
INNER JOIN TB_PEDIDO P
ON P.CODVENDEDOR = V.CODVENDEDOR 
GROUP BY P.CODVENDEDOR, V.NOMEVENDEDOR 
HAVING COUNT(V.CODVENDEDOR) < 2;

SELECT V.NOMEVENDEDOR, COUNT(V.CODVENDEDOR) AS CONTAGEM FROM TB_VENDEDOR V
INNER JOIN (SELECT CODVENDEDOR, COUNT(CODVENDEDOR) AS CONTAGEM FROM TB_PEDIDO GROUP BY CODVENDEDOR HAVING COUNT(CODVENDEDOR) < 2) P
ON P.CODVENDEDOR = V.CODVENDEDOR 
GROUP BY P.CODVENDEDOR, V.NOMEVENDEDOR;

SELECT CODVENDEDOR, COUNT(CODVENDEDOR) AS CONTAGEM FROM TB_PEDIDO GROUP BY CODVENDEDOR HAVING COUNT(CODVENDEDOR) < 2;

--5. Quais os vendedores não fizeram nenhum pedido no mês de maio/2021

SELECT V.NOMEVENDEDOR FROM TB_VENDEDOR V
INNER JOIN (SELECT CODVENDEDOR FROM TB_PEDIDO WHERE EXTRACT(MONTH FROM PRAZO_ENTREGA) <> 5 AND EXTRACT(YEAR FROM PRAZO_ENTREGA) = 2021 GROUP BY CODVENDEDOR) P
ON V.CODVENDEDOR = P.CODVENDEDOR;

SELECT CODVENDEDOR FROM TB_PEDIDO WHERE EXTRACT(MONTH FROM PRAZO_ENTREGA) <> 5 AND EXTRACT(YEAR FROM PRAZO_ENTREGA) = 2021
GROUP BY CODVENDEDOR;

--6. Listar o nome do vendedor que mais fez pedidos.

SELECT V.NOMEVENDEDOR, PEDIDOS FROM TB_VENDEDOR V
INNER JOIN (SELECT CODVENDEDOR, COUNT(CODVENDEDOR) AS PEDIDOS FROM TB_PEDIDO GROUP BY CODVENDEDOR ORDER BY COUNT(CODVENDEDOR) DESC FETCH FIRST 1 ROW ONLY) P
ON V.CODVENDEDOR = P.CODVENDEDOR;

